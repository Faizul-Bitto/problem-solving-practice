#include <bits/stdc++.h>
using namespace std;

int main()
{
    long long int X, Y;
    cin >> X >> Y;

    cout << X << " + " << Y << " = " << X + Y << endl;
    cout << X << " * " << Y << " = " << X * Y << endl;
    cout << X << " - " << Y << " = " << X - Y;

    return 0;
}